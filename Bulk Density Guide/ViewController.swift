        aboutPopOver.alpha = 0
        
        UIView.animate(withDuration: 0.4) {
            self.visualEffectView.effect = self.effect
            self.aboutPopOver.alpha = 1
            self.aboutPopOver.transform = CGAffineTransform.identity
        }
    }
    
