extension ViewController: UITableViewDelegate {
    
    func tableView(_ tableView: UITableView, didSelectRowAt indexPath: IndexPath) {
        
        tableView.deselectRow(at: indexPath, animated: true)
        print(self.materialData[indexPath.row], "selected!")
    }
    
    func tableView(_ tableView: UITableView, trailingSwipeActionsConfigurationForRowAt indexPath: IndexPath)
        -> UISwipeActionsConfiguration? {
            let favoriteAction = UIContextualAction(style: .normal, title: "Bookmark") { (_, _, completionHandler) in
                var data: String
                if self.searching {
                    data = self.searchMaterial[indexPath.row]
                } else {
                    data = self.materialData[indexPath.row]
                }
                print(data)
                sharedData.append(data)
                saveArray()
                completionHandler(true)
            }
            if #available(iOS 13.0, *) {
                favoriteAction.image = UIImage(systemName: "bookmark")
            } else {
                // fall back to default action
            }
            favoriteAction.backgroundColor = .systemOrange
            let configuration = UISwipeActionsConfiguration(actions: [favoriteAction])
            return configuration
    }
    
}

extension ViewController: UITableViewDataSource {
    
    func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        if searching {
            return searchMaterial.count
        } else {
            return materialData.count
        }
    }
    
    func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "cell", for: indexPath)
        cell.textLabel?.numberOfLines = 0
        if searching {
            cell.textLabel?.text = searchMaterial[indexPath.row]
        } else {
            cell.textLabel?.text = materialData[indexPath.row]
        }
        return cell
    }
    
}

